package com.fujitsu.vdmj.mapper;

/**
 * An exception indicating there was a fatal error in the ClassMapper. More detail
 * should have been printed on stderr. The analysis being constructed cannot be used.
 */
public class ClassMapperException extends RuntimeException
{
	private static final long serialVersionUID = 1L;

	public ClassMapperException(String config)
	{
		super("ClassMapper has fatal errors: " + config);
	}
}
